#include <iostream>

int main() {

  // representing a symbol
  char comma = ',';
  std::cout << comma << std::endl;

  // representing a letter
  char a = 'a';
  std::cout << a << std::endl;

  // character representation of a number
  char one = '1';
  std::cout << one << std::endl;

  /* the numerical representation of a number would look similar to the character representation when printed out, but these are of different data types and hence different values */
  int numOne = 1;
  std::cout << numOne << std::endl;
  
}
