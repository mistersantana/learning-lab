#include <iostream>

int main(){

  // a double variable
  double price = 19.99;

  /* the default value of a double is either zero or a very small number that is close to zero */
  double balance;
  std::cout << balance << std::endl;
  
  // assign a positive value
  balance = 100;
  std::cout << balance << std::endl;

  // assign a negative value
  balance = -5.95;
  std::cout << balance << std::endl;

  // float type has half the precision of a double type
  float bankBalance;

  // long double type is used for higher precision
  long double largeBalance;
}
